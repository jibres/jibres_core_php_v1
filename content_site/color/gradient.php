	public static function sample_selector()
	{
		$html  = '';
		 $html .= '<div class="text-center">';
	    {
	    	foreach (self::sample() as $gradient)
	    	{
	    		$html .= '<div class="grid auto-cols-auto">';
	    		{
					$myColor = implode(' ', ['bg-'. a($gradient, 'background_gradient_type'), a($gradient, 'background_gradient_from'), a($gradient, 'background_gradient_via'), a($gradient, 'background_gradient_to')]);
					$json    = json_encode(array_merge($gradient, ['need_redirect' => true]));
					$html    .= "<a data-ajaxify data-data='$json' title='$myColor' class='h-12 ". $myColor. "'>$myColor</a>";
	    		}
	    		$html .= '</div>';
	    	}
	    }
	    $html .= '</div>';

	    return $html;
	}

